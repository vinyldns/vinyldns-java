package vinyldns.java.model.record.data;

public class TXTData implements RecordData {
    private String text;

    public TXTData() {
    }

    public TXTData(String text) {
        this.text = text;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    @Override
    public String toString() {
        return "TXTData{" +
                "text='" + text + '\'' +
                '}';
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        TXTData aData = (TXTData) o;

        return text.equals(aData.text);
    }

    @Override
    public int hashCode() {
        return text.hashCode();
    }
}
